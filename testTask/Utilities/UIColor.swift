import UIKit

extension UIColor {
    
    @nonobjc class var frenchBlue: UIColor {
        return UIColor(red: 62.0 / 255.0, green: 81.0 / 255.0, blue: 181.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var veryLightGrey: UIColor {
        return UIColor(white: 196.0 / 255.0, alpha: 1.0)
    }
    
}
